package ejercicio5;
import java.util.ArrayList;

public class MayorNumero {
	int mayor = 0;
	public int verificarMayor(ArrayList<Integer> ListaNumeros) {
		for (int i = 0; i < ListaNumeros.size(); i++) {
			if(ListaNumeros.get(i) > mayor ) {
				mayor = ListaNumeros.get(i);
			}
		} 
	return mayor;
	}
}
